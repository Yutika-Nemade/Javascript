// while loop
let i = 0
while (i <= 10) {
    console.log(`Value of index is ${i}`)
    i = i + 2
}

let myArr = [10,20,30,40,50]
let index = 0
while(index < myArr.length){
    console.log(`Value is ${myArr[index]}`)
    index++
}


